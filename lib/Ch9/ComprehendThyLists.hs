module Ch9.ComprehendThyLists where

mySqr :: [Integer]
mySqr = [x ^ 2 | x <- [1 .. 10]]

-- 1. [4,16,36,64,100]
r1 :: [Integer]
r1 = [x | x <- mySqr, even x]

-- 2. [(1,64),(1,81),(1,100),(4,64),(4,81),(4,100),(9,64),(9,81),(9,100),(16,64),(16,81),(16,100),(25,64),(25,81),(25,100),(36,64),(36,81),(36,100),(49,64),(49,81),(49,100)]
r2 :: [(Integer, Integer)]
r2 = [(x, y) | x <- mySqr, x < 50, y <- mySqr, y > 50]

-- 3. [(1,64),(1,81),(1,100),(4,64),(4,81)]
r3 :: [(Integer, Integer)]
r3 = take 5 [(x, y) | x <- mySqr, x < 50, y <- mySqr, y > 50]